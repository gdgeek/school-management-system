<?php

declare(strict_types=1);

/**
 * Route Constants Generator
 *
 * Reads config/routes.php and generates src/Route/RouteNames.php
 * with public const string constants for all route names.
 *
 * Usage:
 *   php bin/generate-route-constants.php
 */

$backendDir = dirname(__DIR__);
$routesFile = $backendDir . '/config/routes.php';
$outputFile = $backendDir . '/src/Route/RouteNames.php';

if (!file_exists($routesFile)) {
    fwrite(STDERR, "Error: routes file not found at {$routesFile}\n");
    exit(1);
}

$routes = require $routesFile;

if (!is_array($routes)) {
    fwrite(STDERR, "Error: routes.php did not return an array\n");
    exit(1);
}

// Extract all route names
$names = [];
foreach ($routes as $route) {
    if (isset($route['name']) && is_string($route['name']) && $route['name'] !== '') {
        $names[] = $route['name'];
    }
}

if (empty($names)) {
    fwrite(STDERR, "Warning: no route names found\n");
    exit(0);
}

// Convert route name to UPPER_SNAKE_CASE constant name
// e.g. 'schools.list' → 'SCHOOLS_LIST'
//      'groups.addMember' → 'GROUPS_ADD_MEMBER'
function toConstantName(string $routeName): string
{
    // Replace dots and hyphens with underscores
    $name = str_replace(['.', '-'], '_', $routeName);
    // Insert underscore before uppercase letters (camelCase → snake_case)
    $name = preg_replace('/([a-z])([A-Z])/', '$1_$2', $name);
    return strtoupper($name);
}

// Build constants
$constants = [];
foreach ($names as $routeName) {
    $constName = toConstantName($routeName);
    $constants[$constName] = $routeName;
}

// Generate PHP file content
$lines = [];
$lines[] = '<?php';
$lines[] = '';
$lines[] = '// @generated — run bin/generate-route-constants.php to regenerate';
$lines[] = '// Do not edit this file manually.';
$lines[] = '';
$lines[] = 'declare(strict_types=1);';
$lines[] = '';
$lines[] = 'namespace App\Route;';
$lines[] = '';
$lines[] = '/**';
$lines[] = ' * Route name constants for IDE autocomplete support.';
$lines[] = ' *';
$lines[] = ' * @generated Run `php bin/generate-route-constants.php` to regenerate after';
$lines[] = ' *            modifying config/routes.php.';
$lines[] = ' */';
$lines[] = 'final class RouteNames';
$lines[] = '{';

foreach ($constants as $constName => $routeName) {
    $lines[] = "    public const string {$constName} = '{$routeName}';";
}

$lines[] = '}';
$lines[] = '';

$content = implode("\n", $lines);

// Ensure output directory exists
$outputDir = dirname($outputFile);
if (!is_dir($outputDir)) {
    mkdir($outputDir, 0755, true);
}

file_put_contents($outputFile, $content);

$count = count($constants);
echo "Generated {$count} route name constant(s) → {$outputFile}\n";
